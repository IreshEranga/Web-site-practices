//---------------logical operators----------------
//---------------------------------------------------

var a = 20;
var b = 10;
var c = 5;

//and operator
if(a == 10 && a == b){
    console.log('a equal to 10 and a equal to b');
}

//or operator
if(a > 10 || c > b){
    console.log('a greater than 10 or c greater than b');
}

//not operator
if(a != 10){
    console.log('a is not equal to 10');
}